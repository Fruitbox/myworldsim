
package MyWorldSim;

public class entryDirection 
{

	private short width;

	private short length;
	
	
	public short getWidth() {
		return width;
	}
	public short getLength() {
		return length;
	}
	
	
	
	public entryDirection(short width, short length )
	{
		this.width = width;

		this.length  = length;
		
	}
}









